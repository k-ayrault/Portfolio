    <h2>Application de suivie qualité</h2>
</br>

    <div id="description">
        <p>&emsp;&emsp;Ce projet a été fait en collaboration avec <a href="https://www.linkedin.com/in/guillaume-coulaud-123a57197/" style="color:white;">Guillaume COULAUD</a> et
        <a href="https://fr.linkedin.com/in/alexandre-mignot-971a53207" style="color:white;">Alexandre MIGNOT</a>, lors de notre deuxième année de DUT Informatique afin de répondre à la demande d'un client, l'entreprise LEGRAND,  pour notre module de projet tuteuré.
        <br/>
        <br/>
        &emsp;&emsp;Nous devons réaliser une application web afin de permettre aux employés de l'entreprise LEGRAND de contrôler la qualité des pièces qu'ils confectionnent tout du long le processus.
        Nous devons chercher dans un pdf ou un power-point le numéro de critère du contrôle qualité afin d'afficher critère par critère, le contrôle adéquat. Et enfin, nous devons 
        enregistrer dans la base de donnée le résultat de ces contrôles.
        <br/>
        <br/>
        &emsp;&emsp;Malheureusement, le projet n'a pu être terminé à temps à cause de l'utilisation d'un PDF. En effet, nous ne sommes pas arrivés à scinder un PDF pour 
        afficher uniquement les parties qui nous interessaient sur celui-ci et nous n'avons pas trouvés de solution pour convertir un PowerPoint en PDF sans que des problèmes 
        techniques interviennent. Cependant, ce projet m'a permit de travailler pour la première fois avec un véritable client. De plus, ce projet m'a permit de découvrir le 
        langage Javascript, certaines bibliothèque pour lire et afficher un PDF ainsi que différentes technique de conversion d'un PowerPoint.</p>

        <br/>
        <br/>
        <p>Compétences développées : </p>
        <ul>
            <li>HTML5/CSS3</li>
            <li>PHP</li>
            <li>Javascript</li>
            <li>Création d'une documentation de logiciel</li>
            <li>Analyse des attentes d'un client</li>
            <li>Travail d'équipe</li>
        </ul>
